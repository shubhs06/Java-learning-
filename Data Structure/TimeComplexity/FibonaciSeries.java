public class FibonaciSeries {

    public static void main(String[] args) {
        int a =5 ;                      //assignment statement

        int fibo =0;                   // assignment statement

        for(int i = 0; i <=a; i++ ){

             fibo += i;                              //loop =n
             System.out.println(fibo + " ");
        }
    }
}

